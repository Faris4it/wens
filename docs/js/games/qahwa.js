  const PER_ROUND = 8; // أسئلة لكل فنجال — قصيرة ومركزة

  /* ─────────── حالة الجلسة ─────────── */
  const state = { session: 'group', level: 1, deck: [], pos: 0 };

  const sessions = chipGroup($('#sessionChips'));
  const levels = chipGroup($('#levelChips'));

  const buildDeck = () => {
    state.deck = shuffle(BANKS[state.session][state.level]).slice(0, PER_ROUND);
    state.pos = 0;
  };

  const renderCard = (animate = true) => {
    const card = $('#qCard');
    $('#qText').textContent = state.deck[state.pos];
    $('#qCount').textContent = `${arNum(state.pos + 1)} من ${arNum(state.deck.length)}`;
    $('#levelLabel').textContent = LEVEL_NAMES[state.level];
    if (animate) {
      card.classList.remove('is-swap');
      void card.offsetWidth; // إعادة تشغيل الحركة
      card.classList.add('is-swap');
    }
  };

  /* ─────────── التدفق ─────────── */
  $('#pourBtn').addEventListener('click', () => {
    state.session = sessions.value();
    state.level = Number(levels.value());
    buildDeck();
    renderCard(false);
    show('card');
  });

  $('#nextQ').addEventListener('click', () => {
    state.pos++;
    if (state.pos < state.deck.length) {
      renderCard();
    } else {
      renderEnd();
      show('end');
    }
  });

  $('#endSession').addEventListener('click', () => {
    renderEnd();
    show('end');
  });

  const renderEnd = () => {
    const hasNext = state.level < 3;
    $('#nextLevel').textContent = hasNext
      ? `نكمّل؟ ${LEVEL_NAMES[state.level + 1]}`
      : 'نعيدها من الفنجال الأول';
  };

  $('#nextLevel').addEventListener('click', () => {
    state.level = state.level < 3 ? state.level + 1 : 1;
    levels.set(String(state.level));
    buildDeck();
    renderCard(false);
    show('card');
  });
